"""Diccionarios en Python"""

"""Los nombres de los key van a ir escritos siempre en minúsculas (Por convención)"""

var_1 = {"nombre": "Margarita", "edad": 27, "promedio": 15}

print("Nuestro primer diccioario tiene el siguiente contenido: {}".format(var_1))

"""Agregamos elementos en un nuevo key a mi dicconario"""

var_1["distrito"] = "Lince"
var_1["hijo"] = True

print("Mi diccionario actualizado tiene los siguiente valores: {}".format(var_1))
